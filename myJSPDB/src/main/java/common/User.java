package common;

public class User {
	private String customer_user_id;
	private String customer_user_pwd;
	private String customer_user_name;
	private String customer_user_email;
	private String customer_user_phoneNumber;
	private String customer_user_company;
	
	public String getCustomer_user_phoneNumber() {
		return customer_user_phoneNumber;
	}
	public void setCustomer_user_phoneNumber(String customer_user_phoneNumber) {
		this.customer_user_phoneNumber = customer_user_phoneNumber;
	}
	public String getCustomer_user_company() {
		return customer_user_company;
	}
	public void setCustomer_user_company(String customer_user_company) {
		this.customer_user_company = customer_user_company;
	}
	public String getCustomer_user_id() {
		return customer_user_id;
	}
	public void setCustomer_user_id(String customer_user_id) {
		this.customer_user_id = customer_user_id;
	}
	public String getCustomer_user_pwd() {
		return customer_user_pwd;
	}
	public void setCustomer_user_pwd(String customer_user_pwd) {
		this.customer_user_pwd = customer_user_pwd;
	}
	public String getCustomer_user_name() {
		return customer_user_name;
	}
	public void setCustomer_user_name(String customer_user_name) {
		this.customer_user_name = customer_user_name;
	}
	public String getCustomer_user_email() {
		return customer_user_email;
	}
	public void setCustomer_user_email(String customer_user_email) {
		this.customer_user_email = customer_user_email;
	}
	
	
}
